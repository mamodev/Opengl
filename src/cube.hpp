class Cube : public Drawable {
  public:
    static Mesh * mesh;

    glm::vec3 Pos, scale;

    Cube (glm::vec3 Pos, glm::vec3 scale) {

      this->Pos = Pos;
      this->scale = scale;

      this->model = glm::mat4(1.0f);
      this->model = glm::translate(this->model, this->Pos);
      this->model = glm::scale(this->model, this->scale);

      if(!Cube::mesh) {
        Cube::mesh = new Mesh(vertices, sizeof(vertices) / sizeof(GLfloat));
      }
    }

    ~Cube () {
      // if(Cube::mesh) {
      //   delete Cube::mesh;
      //   Cube::mesh = NULL;
      // }
    }

    void draw(Shader& shader, glm::mat4 * model) {
      glm::mat4 mod = this->model;
      if(model) 
        mod = mod * (*model);

      if (this->material) {
        this->material->bind(shader);
      }
        
      Cube::mesh->draw(shader, &mod);
    }
};


Mesh * Cube::mesh = NULL;


